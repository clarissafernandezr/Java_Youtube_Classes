/**
 * interface to determine that comments and videos are likeable.
 * @author Clarissa Fernandez
 * @version 1
 */
public interface Likeable {
    /**
     * getlikes method default.
     * @return int number of likes
     */
    int getLikes();
    /**
     * getdislikes method default.
     * @return number of dislikes
     */
    int getDislikes();
}
